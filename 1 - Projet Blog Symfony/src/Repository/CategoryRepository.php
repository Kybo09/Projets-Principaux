    public function getCategoryWithPost(){
        $q = $this->createQueryBuilder("c")
            ->addSelect('p')
            ->innerJoin('c.posts', 'p')
            ->andWhere('p.publishedAt <= :now')
            ->setParameter('now', new \DateTime())
            ->groupBy('c.id')
            ->orderBy('c.name', 'ASC');
        return $q->getQuery()
            ->getResult();
    }


